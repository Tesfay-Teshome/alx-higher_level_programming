#!/usr/bin/python3
def add(a, b):
    """ My addition function

    Args:
        a: first integer
        b: second integer

    Return:
        The return value. a + b
    """
    return (a + b)

def sub(a, b):
    """ My subtraction function

    Args:
        a: first integer
        b: second integer

    Retruns:
         The return value. a - b
    """
    return (a - b)

def mul(a, b):
    """ The Multiplication function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a * b
    """
    return (a * b)

def div(a, b):
    """ The Division function

    Args:
        a: first integer
        b: second integer

    Returns:
        The return value. a / b
    """
    return (a / b)
